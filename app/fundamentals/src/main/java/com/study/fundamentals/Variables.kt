package com.study.fundamentals

fun main() {
    /**
     * var - mutable variable
     * val immutable variable
     * use val as a default and var only when needed. This is a best practice
     */
    val name = "Daniel"
    val age: Int = 10
    val myByte: Byte = 127
    val myShort: Short = 1
    val myLong: Long = 1L
    val price: Float = 1.5f
    print("Hello name is $name and I am $age years old")
}
